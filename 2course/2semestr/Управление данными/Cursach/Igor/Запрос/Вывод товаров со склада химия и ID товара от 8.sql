Select Склады.[Номер склада], Склады.Название,ID as [Код товара], Товары.Название, Описание
From [Товары на складе] 
	Inner join Склады on [Товары на складе].[Номер склада]=Склады.[Номер склада]
	Inner join Товары on [Товары на складе].[ID товара]=Товары.ID
Where Склады.[Номер склада]=2 and ID>=8
Group by Склады.[Номер склада], Склады.Название,ID, Товары.Название, Описание