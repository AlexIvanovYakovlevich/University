Select Производители.ID as [Код производителя], Производители.Название, Производители.Описание, Адрес, Телефон, Товары.ID as [Код товара],
Товары.Описание, Товары.Название, [Средняя закупочная цена], Скидка, [Условия хранениня], 
[Срок годности], [Номер склада], [Минимальный остаток], Количество
From Товары 
	Inner join Производители on Производители.ID=Товары.[ID Производителя]
	Inner join [Товары на складе] on Товары.ID=[Товары на складе].[ID товара]
Where Производители.ID=6